    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String user = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());
        
        if(ManagerDB.getInstance().verifyUserPassword(user, password) == true){
            
            JOptionPane.showMessageDialog(this, "Login Successful!");
           
            this.setVisible(false);//this.dispose()
            new Window2(user);
        }else{
            JOptionPane.showMessageDialog(this, "Incorrect user or password!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String user = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());
        
        if(ManagerDB.getInstance().userExist(user) == true){
            JOptionPane.showMessageDialog(this, "User exist");
           
        }else{ 
            ManagerDB.getInstance().addUser(user, password);
           
        }
        
        jTextField1.setText(null);
        jPasswordField1.setText(null);
    }//GEN-LAST:event_jButton2ActionPerformed

  

